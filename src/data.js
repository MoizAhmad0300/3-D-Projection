// Helper to process raw companies list into normalized structures
export function processGroupData(companies) {
    const personToCompanies = new Map();
    const allPersonsSet = new Set();
    companies.forEach(comp => {
        comp.directors.forEach(person => {
            allPersonsSet.add(person);
            if (!personToCompanies.has(person)) personToCompanies.set(person, []);
            personToCompanies.get(person).push(comp.name);
        });
    });
    const personList = Array.from(allPersonsSet).sort();
    const companyNodes = companies.map(comp => ({ id: comp.id, name: comp.name, type: 'company', compType: comp.type, directors: comp.directors }));
    const personNodes = personList.map(p => ({ id: `p_${p.replace(/\s/g, '')}`, name: p, type: 'person', companies: personToCompanies.get(p) || [] }));
    const graphNodes = [...companyNodes, ...personNodes];
    const links = [];
    companies.forEach(comp => {
        comp.directors.forEach(person => {
            const personNode = personNodes.find(p => p.name === person);
            if (personNode) {
                links.push({ source: comp.id, target: personNode.id, sourceType: 'company', targetType: 'person' });
            }
        });
    });
    const nodeMap = new Map();
    graphNodes.forEach(node => nodeMap.set(node.id, node));

    return {
        companiesData: companies,
        personToCompanies,
        personList,
        companyNodes,
        personNodes,
        graphNodes,
        links,
        nodeMap
    };
}

